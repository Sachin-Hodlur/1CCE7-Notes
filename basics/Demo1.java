class Demo1
{
	public static void main(String[] args)
	{
		System.out.println("Program Started");
		System.out.println(50); //50
		System.out.println("Java"); //Java
		System.out.println(50+50); //100
		System.out.println("Java"+"Developer"); //JavaDeveloper
		System.out.println(50+"Java"); //50Java
		System.out.println(50+50+"Java"); //100Java
		System.out.println("Java"+50+50); //Java5050
		System.out.println("50+50");//50+50
		System.out.println("Java"+50+"Developer"); //Java50Developer
		System.out.println("Mar"+"Java"+50+50); //MarJava5050
		System.out.println("Program Ended");
	}
}